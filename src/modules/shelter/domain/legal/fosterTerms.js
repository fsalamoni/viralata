export const FOSTER_TERMS_SHORT_LABEL = 'Termo de Lar Temporário';

export function getFosterTermsLabel() {
  return `${FOSTER_TERMS_SHORT_LABEL} (versão ${FOSTER_TERMS_VERSION})`;
}

export function isCurrentFosterTermsVersion(acceptedVersion) {
  return acceptedVersion === FOSTER_TERMS_VERSION;
}

/**
 * Helper: constrói o payload dos campos de aceite para gravar no
 * doc do foster. Usado pelo `fosterService.acceptFoster`.
 *
 * @param {string} signatureText - nome completo do LT (assinatura)
 * @returns {{
 *   foster_terms_accepted_at: string,
 *   foster_terms_version: string,
 *   foster_signature_text: string,
 * }}
 */
export function buildFosterTermsAcceptance(signatureText) {
  if (!signatureText || typeof signatureText !== 'string' || signatureText.trim().length < 3) {
    throw new Error('Assinatura (signature_text) deve ter no mínimo 3 caracteres.');
  }
  return {
    foster_terms_accepted_at: new Date().toISOString(),
    foster_terms_version: FOSTER_TERMS_VERSION,
    foster_signature_text: signatureText.trim(),
  };
}
